    private void btnBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnBuscarKeyPressed
        // TODO add your handling code here:
        String apellidoS = txtApellido.getText();
        if(apellidoS.length()==0)
        {
            JOptionPane.showMessageDialog(null, "No ha ingresado ningun dato", "ERROR", JOptionPane.WARNING_MESSAGE);
            txtApellido.requestFocus();
        }
        else
        {
            if(apellidoS.length()>50)
            {
                JOptionPane.showMessageDialog(null, "Se ha excedido el numero de caracteres", "ERROR", JOptionPane.WARNING_MESSAGE);
                txtApellido.requestFocus();
            }
            else
            {
        
        Paciente buscar = new Paciente(null, null, null, null, null);
        buscar.BuscarPaciente(modelo, txtApellido.getText());
        txtApellido.setText("");
        txtApellido.requestFocus();
        Paciente limpiartabla = new Paciente(null, null, null, null, null);
        limpiartabla.Limpiar(modelo);
            }
        }
    }//GEN-LAST:event_btnBuscarKeyPressed

